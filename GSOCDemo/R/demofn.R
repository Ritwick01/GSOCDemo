#' @title
#' Returns the square of the value passed if numeric value is passed
#' @author
#' Ritwick Pal \email{ritwick20461@iiitd.ac.in}
#'
#' @param Value numeric value
#' @return square of the numeric value
#' @export
Function1<-function(Value)
{

  stopifnot("Input must be numeric" = is.numeric(Value))

  return(Value^2)
}
